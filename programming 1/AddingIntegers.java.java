public class AddingIntegers{
	public static void main(String[]args){
		int number1 = 100;
		int number2 = 500;
		int number3 = 1000;

		int sum = number1 + number2 + number3; // this is for addition :)

		int sampleNumber = 5000;

		int product = number3 * sum * sampleNumber; // this is for multiplication

		System.out.print("the product is"+product);
		System.out.print("the sum is"+sum);


	}


}