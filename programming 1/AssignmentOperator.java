public class AssignmentOperator{
	public static void main (String[] Jareth){


		int a = 10;
		a += 5; // addded by 5

		System.out.println( "a: " +a );

		int b = 10;
		b -= 5; // subtracted by 5

		System.out.println( "b: " +b );

		int c = 10;
		c *= 5; // multiplied by 5

		System.out.println( "c: " +c );

		int d = 10;
		d /= 5; // divided by 5

		System.out.println( "d: " +d );

		int e = 10;
		e &= 5; // remainder (& = Modulus)

		System.out.println( "e: " +e );
	
	}
}	